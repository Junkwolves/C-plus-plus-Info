#include <iostream>

using namespace std;

int main()
{

	int a1 = 0;

	cout << endl << "     | ---------------------- |  " << endl;

	cout << endl << "     |  a1 = 0;  |  a1++ = " << a1++ << "  |  " << endl;

	int a2 = 0;

	cout << endl << "     |  a2 = 0;  |  ++a2 = " << ++a2 << "  |  " << endl;

	cout << endl << "     | ---------------------- |  " << endl;

	cout << endl << "     |  a1 = 1;  |  a1-- = " << a1-- << "  |  " << endl;

	cout << endl << "     |  a2 = 1;  |  --a2 = " << --a2 << "  |  " << endl;

	cout << endl << "     | ---------------------- |  " << endl;

	return 0;
}